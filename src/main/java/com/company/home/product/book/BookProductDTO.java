package com.company.home.product.book;

public class BookProductDTO {
	private Integer product_id;
	private String product_name;
	private Double product_interest;
	private Integer product_is_sale;
	private String product_detail;
	private String product_condition;
	private Integer product_transfer_limit;
	
	public Integer getProduct_id() {
		return product_id;
	}
	public void setProduct_id(Integer product_id) {
		this.product_id = product_id;
	}
	public String getProduct_name() {
		return product_name;
	}
	public void setProduct_name(String product_name) {
		this.product_name = product_name;
	}
	public Double getProduct_interest() {
		return product_interest;
	}
	public void setProduct_interest(Double product_interest) {
		this.product_interest = product_interest;
	}
	public Integer getProduct_is_sale() {
		return product_is_sale;
	}
	public void setProduct_is_sale(Integer product_is_sale) {
		this.product_is_sale = product_is_sale;
	}
	public String getProduct_detail() {
		return product_detail;
	}
	public void setProduct_detail(String product_detail) {
		this.product_detail = product_detail;
	}
	public String getProduct_condition() {
		return product_condition;
	}
	public void setProduct_condition(String product_condition) {
		this.product_condition = product_condition;
	}
	public Integer getProduct_transfer_limit() {
		return product_transfer_limit;
	}
	public void setProduct_transfer_limit(Integer product_transfer_limit) {
		this.product_transfer_limit = product_transfer_limit;
	}
	
	
}
